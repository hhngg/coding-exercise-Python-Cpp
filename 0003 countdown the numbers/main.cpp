//Write a program that prints out the
//calls for a spaceship that is about to
//launch. Countdown the numbers from
//10 to 1 and then write “Liftoff.”

#include <iostream>
using namespace std;

int main() {
  for (int i = 10; i > 0; i--) {
    cout << i << "\n";
  }
  cout << "Liftoff" << endl;
}

